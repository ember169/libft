#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
	return;
}


int	main(int argc, char *argv[])
{
	ft_putstr(argv[0]);
}
