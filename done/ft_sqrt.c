int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	while(i * i != nb)
	{
		if (i * i > nb)
			return(0);
		i++;
	}
	return (i);
}

#include <stdio.h>
int	main()
{
	printf("%d", ft_sqrt(9));
}
